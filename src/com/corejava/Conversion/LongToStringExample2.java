package com.corejava.Conversion;

/* Long.toString() method converts long to String. The toString() 
 * is the static method of Long class.
 * 
 */
public class LongToStringExample2 
{
	public static void main(String args[])
	{  
		long i=8867527402L;  
		String s=Long.toString(i);  
		System.out.println(s);  
		}

}
