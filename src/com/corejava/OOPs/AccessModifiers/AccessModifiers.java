package com.corejava.OOPs.AccessModifiers;

public class AccessModifiers 
{
	/* AccessModifiers : private , protected, public .
	 * 
	 * Access specifier is synonymous of Access modifier.
	 * 
	 * private can be accessed only within the same class.
	 * 
	 * Default can be accessed only within the classes of same package.
	 * 
	 * Protected can be accessed within the classes of same package and in 
	 * classes of other packages only by inheritance, i.e. in subclasses.
	 * 
	 * Public are accessible from everywhere.
	 * 
	 * 
	 */

}
