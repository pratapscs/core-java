package com.corejava.OOPs.Polymorphism;

public class polymorphism 
{
	/* Compile time polymorphism :
	 * Compile time polymorphism can be achieved by using Method overloading 
	 * When a class have same method name with different argument, 
	 * than it is called method overloading.
	 * 
	 * Advantage of method overloading -
	 * Method overloading enables consistency in the naming of methods which 
	 * logically perform almost similar tasks and the only difference is in 
	 * number of arguments. Method overloading enables same method name to be 
	 * reused in program.
	 * 
	 * Method overloading is done to make program logically more readable and
	 * understandable.
	 * 
	 * 
	 * Runtime polymorphism:
	 * Runtime polymorphism can be achieved by using Method overriding 
	 * 
	 * Method of superclass is overridden in subclass to provide more specific
	 * implementation.
	 * 
	 * Call to overloaded method is bonded at compile time.
	 * 
	 * Method overloading concept is also known as compile time polymorphism
	 * in java.
	 * 
	 * Java does not allow overloading by changing the return type, 
	 * though overloaded methods can change the return type.
	 * 
	 * Method overloading is generally done in same class but can also be done
	 * in SubClass.
	 * 
	 * 
	 * 
	 */

}
