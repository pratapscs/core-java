package com.corejava.OOPs.Abstraction;

public class Abstraction 
{
	/* Abstraction :
	 * 
	 *  Abstraction means hiding the implementation.
	 *  
	 *  Abstraction means representing only the essential things without 
	 *  including background details.
	 *  
	 *  In java abstraction can be achieved by using >
	 *  Interfaces and Abstract classes 
	 *  
	 *  Ex:
	 *  When you log into your email, compose and send a mail. Again there is a whole 
	 *  lot of background processing involved, verifying the recipient, sending request 
	 *  to the email server, sending your email. Here you are only interested in composing
	 *  and clicking on the send button. What really happens when you click on the send 
	 *  button, is hidden from you.
	 * 
	 */

}
