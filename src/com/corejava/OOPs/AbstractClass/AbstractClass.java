package com.corejava.OOPs.AbstractClass;

public class AbstractClass 
{
	/* Features of Abstract class in java :
	 * 
	 * 1.Abstract class aren’t purely abstraction in java.
	 * 
	 * 2.It’s mandatory to write abstract keyword to make class abstract.
	 * 
	 * 3.Abstract class can be abstract even without any abstract method.
	 * 
	 * 4.Abstract class can have private, final, abstract, static and instance methods.
	 * 
	 * Note : Method cannot be private and abstract.
	 * 
	 * Method cannot be final and abstract.
	 * 
	 * 5.Abstract class have constructors. 
	 * 
	 * 6.Abstract class  doesn’t allows multiple inheritance in java.
	 * 
	 * 7.Abstract class  implements Interface, can implement more than one 
	 *   interface.
	 *   
	 * 8.If any new instance method is added in Abstract class then all 
	 * concrete classes which extends that abstract class need not to 
	 * provide implementation of newly added instance method. 
	 * (MyClass2.java)
	 * 
	 * If any new abstract method is added in Abstract class then all concrete 
	 * classes which extends that abstract class must provide implementation 
	 * of newly added abstract method. (MyClass1.java)
	 * 
	 * 9.Abstract class can have synchronized instance methods, but abstract
	 *  methods cannot be synchronized.
	 *  concrete class which implements abstract class can make abstract
	 *  methods synchronized.
	 *  
	 * 10.Even if one abstract method is defined in class than class must be declared as abstract.
	 * 
	 * 
	 */

}
