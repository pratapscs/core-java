package com.corejava.OOPs.CallByValueCallByReference;

public class Callbyreference 
{
	/*
	 * In Call by reference, reference of argument is passed to method. 
	 * Therefore, any changes made to the argument in passed method will 
	 * change the value of original argument as well.
	 * 
	 * In java when any changes are made to the argument in passed method  
	 * the value of original argument is not changed. 
	 * 
	 * Hence, Java is NOT call by reference.
	 * 
	 */

}
