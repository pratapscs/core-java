
package com.corejava.OOPs.CovariantReturnType;

public class CovariantReturnType 
{
	/* CovariantReturnType :
	 * Covariant return type were introduced in java 5.
	 * 
	 * Java allow overriding by changing the return type, but only Covariant 
	 * return type are allowed
	 * 
	 * ex:
	 * return type of SuperClass’s myMethod is  SuperClass, and 
	 * Returns new SuperClass
	 * 
	 * return type of SubClass’s   myMethod is  SuperClass, but
	 * Returns new SubClass.
	 * 
	 * Covariant return type in java means that overriding method can 
	 * return SubClass.
	 * 
	 */

}
