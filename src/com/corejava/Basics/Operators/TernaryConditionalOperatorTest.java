package com.corejava.Basics.Operators;

public class TernaryConditionalOperatorTest 
{
    public static void main(String[] args)
    {
           int x=3;
 
           System.out.println( (x >4) ?   "x is > 4"   :   "x is < 4" );
 
           System.out.println( (x==3) ?   "x == 3"  :   "x != 3" );
           
    }
 
}